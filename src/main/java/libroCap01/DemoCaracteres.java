package libroCap01;
/**
 * Clase muestra los numeros del 0 al 5 en caracter
 *
 * @author Erick Nicolas Gonzalez Rojas
 * @version 1.0 19/10/2023
 */
public class DemoCaracteres {
    public static void main(String[] args) {
        for (int i = 0; i < 5 ; i++) {
            System.out.print(i + 'A' + "\t");
            System.out.print(i + "A" + "\t");
            System.out.println();
        }
    }
}
